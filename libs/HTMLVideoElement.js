/**
 * @class HTMLVideoElement
 * DOM video objects expose the HTMLVideoElement interface, which provides special properties
 * (beyond the regular element object and HTMLMediaElement interfaces they also have available 
 * to them by inheritance) for manipulating video objects.
 * @extends HTMLMediaElement
 */

/**
 * @property height	
 * @type {String}
 * Reflects the height HTML attribute, which specifies the height of the display area, in CSS pixels.
 */

/**
 * @property poster
 * @type {String}
 * Reflects the poster HTML attribute, which specifies an image to show while no video data is available.
 */
/**
 * @property videoHeight
 * @type {Number}
 * The intrinsic height of the resource in CSS pixels, taking into account the dimensions, aspect ratio, clean aperture, resolution, and so forth, 
 * as defined for the format used by the resource.If the element's ready state is HAVE_NOTHING, the value is 0.
 */

/**
 * @property videoWidth
 * @type {Number}
 * The intrinsic width of the resource in CSS pixels, taking into account the dimensions, aspect ratio, clean aperture, resolution, and so forth,
 * as defined for the format used by the resource. If the element's ready state is HAVE_NOTHING, the value is 0.
 */

/**
 * @property width
 * @type {String}
 * Reflects the width  HTML attribute, which specifies the width of the display area, in CSS pixels. 
 */

